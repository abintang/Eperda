package com.project.eperda.model;

public class BeritaModel {
    int idBerita;
    String gambarBerita, judulBerita, dateBerita, deskripsiBerita;

    public int getIdBerita() {
        return idBerita;
    }

    public void setIdBerita(int idBerita) {
        this.idBerita = idBerita;
    }

    public String getGambarBerita() {
        return gambarBerita;
    }

    public void setGambarBerita(String gambarBerita) {
        this.gambarBerita = gambarBerita;
    }

    public String getJudulBerita() {
        return judulBerita;
    }

    public void setJudulBerita(String judulBerita) {
        this.judulBerita = judulBerita;
    }

    public String getDateBerita() {
        return dateBerita;
    }

    public void setDateBerita(String dateBerita) {
        this.dateBerita = dateBerita;
    }

    public String getDeskripsiBerita() {
        return deskripsiBerita;
    }

    public void setDeskripsiBerita(String deskripsiBerita) {
        this.deskripsiBerita = deskripsiBerita;
    }
}
